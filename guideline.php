								<h2>6.科大術語篇</h2>
								<hr>
								<p align="justify"></p>
								<p align="justify">
								入左科大， 又點可以唔識科大術語呢？</p>
				
								<h4>i.Grade 神</h4>
								<p>指派grade 派得多A / B range 嘅教授。</p><br>
								
								<h4>ii. killer</h4>
								<p>同Grade 神相反， 指嘅係教授派嘅grade 低過一般水平。 例如overall 分數低過mean 少少就fail 人。</p><br>

								<h4>iii. 摺&amp;頹</h4>
								<p>摺嘅意思類似宅， 指嘅係極少參加大學社交活動， 整日自己一個做野。 例如: 摺拉把即係成日喺拉把入面溫書； 頹就等於無心機做野。</p><br>

								<h4>iv. LT</h4>
								<p>即係lecture theater （演講廳）嘅簡稱。</p><br>
								
								<h4>v. Skywalk</h4>
								<p>即係轉堂嗰陣兜課室而唔行concourse ， 搵出最快嘅路徑去目的地。</p><br>

								<h4>vi. Sor屎 &amp; Freerider</h4>
								<p>Sor屎，即academic source ， 例如功課答案、 notes 等等。<br>
								Freerider 即係指group project 入面唔做野但又坐享其成嘅groupmates 。</p><br>

								<h4>vii. 天地堂&amp;走堂</h4>
								<p>天地堂指一日淨係得上晝下晝兩堂 (或幾堂)， 而中間隔左好好好長嘅時間無堂要上。<br>
								走堂即係指無上到嗰日要上嘅堂 (特別指無合理原因下)。</p><br>

								<h4>viii. 過三爆四，SD，CGPA，TGPA，癲List</h4>
								<p>大學科科用grade 黎計分， A+ = 4.3 grade point （GP） 、 A = 4 、 A- = 3.7 如此類推。 將你一個學期嘅Total Grade Points 除嗰個學期嘅Total Credit 就係你嗰個學期嘅 TGPA ； CGPA 計法同TGPA 一樣， 但計曬所有學期嘅Grade 。<br>
								一個sem 入面如果 TGPA > 3.7 同無fail 任何 course 就會入dean(癲) list 。 過三爆四即係GPA (多指TGPA) >3 or >4，形容極之優異嘅成績。<br>
								UST course 派grade 睇你overall mark 嘅 SD (standard deviation)。 例如，mean = B- / C+， 高mean 一個SD = B+ / A- / A 。</p><br>


								<h4>ix. 莊， 上莊， EXCO， SU， PIC</h4>
								<p>莊即校內學會， 組織等等嘅內閣。<br>
								上莊 (名詞) 指上一屆的學會內閣； 而上莊 (動詞) 即係自己參與候選內閣， 成為EXCO (幹事)。<br>
								SU = Student Union ， 即學生會。<br>
								PIC = 學會活動負責人。</p><br>

								<h4>x. 出pool</h4>
								<p>出pool 即成功由單身 （從來都未拍過拖 = A0）變為拍緊拖。</p><br>
								
								<h4>xi. Re- u</h4>
								<p>全名係reunion ， 即係一大班人喺參加完某個活動之後再約出黎聚會。</p><br>

								<h4>xii. 大學5件事</h4>
								<p>讀書、 上莊、 住宿舍、 搵part-time 、 拍拖。</p><br>
								
